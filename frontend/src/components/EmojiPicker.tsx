export const EmojiPicker: React.FC<EmojiPickerProps> = ({ onEmojiSelect, textareaRef }) => {
  const [open, setOpen] = useState(false);

  const handleEmojiClick = (emoji: string) => {
    onEmojiSelect(emoji);
    setOpen(false);
  };

  // Combine all emojis from all categories into a single array
  const allEmojis = useMemo(() => {
    const emojiSet = new Set<string>();
    Object.values(EMOJI_CATEGORIES).forEach(categoryEmojis => {
      categoryEmojis.forEach(emoji => emojiSet.add(emoji));
    });
    return Array.from(emojiSet);
  }, []);

  return (
    <Popover open={open} onOpenChange={setOpen}>
      <PopoverTrigger asChild>
          <button
            type="button"
            className="h-6 w-6 p-0 absolute top-1/2 -translate-y-1/4 right-2 text-slate-500 hover:text-slate-700 z-10 flex items-center justify-center rounded hover:bg-slate-100 transition-colors focus:outline-none cursor-pointer"
          >
          <Smile className="h-4 w-4" />
        </button>
      </PopoverTrigger>
      <PopoverContent 
        className="w-80 p-0 !h-64 !max-h-64 overflow-hidden flex flex-col !z-[10020]" 
        align="end" 
        side="top" 
        onOpenAutoFocus={(e) => e.preventDefault()}
        style={{ height: '256px', maxHeight: '256px', zIndex: 10020 }}
      >
        <div className="p-2 flex-1 overflow-y-auto min-h-0">
          <table className="w-full border-collapse">
            <tbody>
              {Array.from({ length: Math.ceil(allEmojis.length / 8) }).map((_, rowIndex) => (
                <tr key={`row-${rowIndex}`}>
                  {Array.from({ length: 8 }).map((_, colIndex) => {
                    const emojiIndex = rowIndex * 8 + colIndex;
                    const emoji = allEmojis[emojiIndex];
                    return (
                      <td key={`cell-${rowIndex}-${colIndex}`} className="p-0 border border-transparent">
                        {emoji ? (
                          <button
                            onClick={() => handleEmojiClick(emoji)}
                            className="w-full h-8 text-xl hover:bg-slate-100 rounded transition-colors cursor-pointer flex items-center justify-center"
                            type="button"
                          >
                            {emoji}
                          </button>
                        ) : null}
                      </td>
                    );
                  })}
                </tr>
              ))}
            </tbody>
          </table>
        </div>
      </PopoverContent>
    </Popover>
  );
};

